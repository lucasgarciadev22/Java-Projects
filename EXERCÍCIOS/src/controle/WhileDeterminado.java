package controle;

public class WhileDeterminado {
	public static void main(String[] args) {
		
		int contador=1;//valor inicial
		
		while(contador <=10) {
			System.out.println("Please Stop Me!");//monitora condição de parada
			contador++;//incrementa a cada loop
			
			//vai contar até 20
		}
		

		int contadorb=0;//valor inicial
		
		while(contadorb <=10) {
			System.out.println("Contador: "+contadorb);//monitora condição de parada
			contadorb+=2;//incrementa +2 a cada loop
			
			//vai contar até 20
	}

}
}