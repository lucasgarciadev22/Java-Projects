package controle;

public class For1 {
	public static void main(String[] args) {
		
	//estrutura do laço for (a= definição da variável b= condição c= incremento/alteração por loop)
	
	for(int contador =0; contador <=10; contador ++) {
		System.out.printf("i= %d\n",contador);//vai escreevendo o valor atual da contagem
		
	}
}
}	